	implicit none
#include <petsc/finclude/petscsys.h>
    character(len=*)::  str
    logical         ::  input 
    PetscBool       ::  flag 
    PetscErrorCode  ::  ierr
    call PetscOptionsGetBool(PETSC_NULL_OBJECT,PETSC_NULL_CHARACTER,str,flag,PETSC_NULL_BOOL,ierr)
    !print *,"str=",str," flag=",flag 
    if(flag .eqv. PETSC_TRUE) then
        input=.true.
    else
        input=.false.
    endif
